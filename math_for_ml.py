def dotProduct(vectors):
  size = len(vectors[0])
  for j in vectors:
    if len(j)!=size:
      raise ValueError("Size unqual")
  val = 0
  for i in range(size):
    temp = 1
    for vect in vectors:
      temp *= vect[i]
    val +=temp

  return val

